from collections import Counter

class Solution:
    def countCharacters(self, words: list[str], chars: str) -> int:
        
        chars_dict = dict(Counter(chars))
        length = 0
        
        for word in words:

            temp_dict = dict(Counter(word))
            take_mem = True

            for ch, occr in temp_dict.items():
                
                if(ch in chars_dict and occr <= chars_dict[ch]):
                    pass
                else:
                    take_mem = False
                    break

            if(take_mem):
                length += len(word)

        return length
    
